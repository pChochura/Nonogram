#pragma once
#include <SFML/Graphics.hpp>
#include "../../Utils/ResourceIdentifier.h"

////////////////////////////////////////////////////////////
//	Class used to describe a part of Screen object.
//	It allows to separate modules and work independently.
////////////////////////////////////////////////////////////
class Stage {
public:
	////////////////////////////////////////////////////////////
	//	Invoked once allowing to load necessary resources.
	////////////////////////////////////////////////////////////
	virtual void init(Context*) = 0;

	////////////////////////////////////////////////////////////
	//	Method invoked every frame. Should contain only drawing 
	//	procedures.
	////////////////////////////////////////////////////////////
	virtual void draw(Context*) = 0;

	////////////////////////////////////////////////////////////
	//	Method invoked when SFML event occured.
	////////////////////////////////////////////////////////////
	virtual bool onEvent(Context*, sf::Event) = 0;

	////////////////////////////////////////////////////////////
	//	Setter
	////////////////////////////////////////////////////////////
	void setView(sf::View view);

protected:
	sf::View view;
};