#pragma once
#include <SFML/Graphics.hpp>
#include "Actor.h"
#include "Button.h"

////////////////////////////////////////////////////////////
//	Class describing set of Button objects.
//	It's used to calculate positions and sizes of all
//	children more easily.
////////////////////////////////////////////////////////////
class ButtonGroup : public Actor {
public:
	ButtonGroup(int id, std::vector<Button*> buttons);

	////////////////////////////////////////////////////////////
	//	@inheritDoc
	////////////////////////////////////////////////////////////
	virtual bool onEvent(Context* context, sf::Event) override;

	////////////////////////////////////////////////////////////
	//	@inheritDoc
	////////////////////////////////////////////////////////////
	virtual void draw(Context* context) const override;

private:
	std::vector<Button*> buttons;
};